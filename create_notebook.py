    "# 4. Ensemble and MLflow Logging\n",
    "mlflow.set_experiment(\"Demand_Forecasting\")\n",
    "\n",
    "def train_and_log_product(product_id):\n",
    "    print(f\"\\n--- Training for product {product_id} ---\")\n",
    "    product_df = prepare_product_data(daily_sales, product_id)\n",
    "    \n",
    "    if len(product_df) < 90:\n",
    "        print(f\"Skipping {product_id}: not enough data points ({len(product_df)} days).\")\n",
    "        return None\n",
    "        \n",
    "    with mlflow.start_run(run_name=f\"Product_{product_id}\"):\n",
    "        mlflow.log_param(\"product_id\", product_id)\n",
    "        \n",
    "        # 1. Train Prophet\n",
    "        try:\n",
    "            prophet_model, forecast, p_mape, y_true, p_pred = train_prophet(product_df)\n",
    "            mlflow.log_metric(\"prophet_mape\", p_mape)\n",
    "            print(f\"Prophet MAPE: {p_mape:.2%}\")\n",
    "        except Exception as e:\n",
    "            print(f\"Prophet failed: {e}\")\n",
    "            return None\n",
    "            \n",
    "        final_model_type = \"Prophet\"\n",
    "        final_mape = p_mape\n",
    "        final_pred = p_pred\n",
    "        \n",
    "        # 2. Check MAPE threshold, train LSTM if needed\n",
    "        lstm_model_info = None\n",
    "        if p_mape > 0.10:\n",
    "            print(\"Prophet MAPE > 10%. Training LSTM...\")\n",
