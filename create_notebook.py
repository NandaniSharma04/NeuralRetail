    "                print(f\"Ensemble MAPE: {ens_mape:.2%}\")\n",
    "                \n",
    "                # Select best model for this product\n",
    "                best_mape = min(p_mape, l_mape, ens_mape)\n",
    "                if best_mape == ens_mape:\n",
    "                    final_model_type = \"Ensemble\"\n",
    "                    final_mape = ens_mape\n",
    "                    final_pred = ens_pred\n",
    "                elif best_mape == l_mape:\n",
    "                    final_model_type = \"LSTM\"\n",
    "                    final_mape = l_mape\n",
    "                    final_pred = l_pred\n",
    "                    \n",
    "                lstm_model_info = {'model': lstm_model, 'scaler': scaler}\n",
    "                \n",
