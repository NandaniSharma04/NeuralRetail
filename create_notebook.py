    "# 2. Prophet Baseline Model\n",
    "def train_prophet(product_df, test_days=30):\n",
    "    # Prophet requires columns 'ds' (date) and 'y' (target)\n",
    "    prophet_df = product_df[['date', 'quantity']].rename(columns={'date': 'ds', 'quantity': 'y'})\n",
    "    \n",
    "    # Split into train and validation (last 30 days)\n",
    "    if len(prophet_df) <= test_days:\n",
    "        raise ValueError(\"Not enough data to split into train and test\")\n",
    "        \n",
    "    train = prophet_df.iloc[:-test_days]\n",
    "    test = prophet_df.iloc[-test_days:]\n",
    "    \n",
    "    model = Prophet(yearly_seasonality=True, weekly_seasonality=True, daily_seasonality=False)\n",
    "    model.fit(train)\n",
    "    \n",
    "    future = model.make_future_dataframe(periods=test_days)\n",
    "    forecast = model.predict(future)\n",
    "    \n",
    "    # Calculate MAPE\n",
    "    y_true = test['y'].values\n",
    "    y_pred = forecast['yhat'].iloc[-test_days:].values\n",
    "    \n",
    "    # Handle zeros in denominator for MAPE\n",
    "    non_zero_idx = y_true != 0\n",
    "    if sum(non_zero_idx) == 0:\n",
    "        mape = 0.0\n",
    "    else:\n",
    "        mape = mean_absolute_percentage_error(y_true[non_zero_idx], y_pred[non_zero_idx])\n",
    "        \n",
    "    return model, forecast, mape, y_true, y_pred"
