    "                lstm_model, scaler, l_mape, _, l_pred = train_lstm(product_df)\n",
    "                mlflow.log_metric(\"lstm_mape\", l_mape)\n",
    "                print(f\"LSTM MAPE: {l_mape:.2%}\")\n",
    "                \n",
    "                # Ensemble (Simple Average)\n",
    "                ens_pred = (p_pred + l_pred) / 2\n",
    "                non_zero_idx = y_true != 0\n",
    "                ens_mape = mean_absolute_percentage_error(y_true[non_zero_idx], ens_pred[non_zero_idx]) if sum(non_zero_idx) > 0 else 0\n",
