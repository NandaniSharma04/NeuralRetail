    "def train_lstm(product_df, test_days=30, seq_length=30):\n",
    "    # Scale data\n",
    "    scaler = MinMaxScaler()\n",
    "    data_scaled = scaler.fit_transform(product_df[['quantity']].values)\n",
    "    \n",
    "    if len(data_scaled) <= test_days + seq_length:\n",
    "        raise ValueError(\"Not enough data for LSTM\")\n",
    "        \n",
    "    train_data = data_scaled[:-test_days]\n",
    "    test_data = data_scaled[-(test_days + seq_length):]\n",
    "    \n",
    "    train_dataset = TimeSeriesDataset(train_data, seq_length)\n",
    "    train_loader = DataLoader(train_dataset, batch_size=32, shuffle=True, num_workers=0)\n",
    "    \n",
    "    model = LSTMForecaster()\n",
    "    \n",
    "    # Train using PyTorch Lightning (uses GPU if available in Colab)\n",
    "    trainer = pl.Trainer(max_epochs=50, accelerator='auto', devices='auto', enable_progress_bar=False, enable_model_summary=False)\n",
    "    trainer.fit(model, train_loader)\n",
    "    \n",
    "    # Predict\n",
    "    model.eval()\n",
    "    test_dataset = TimeSeriesDataset(test_data, seq_length)\n",
    "    test_loader = DataLoader(test_dataset, batch_size=1, shuffle=False)\n",
    "    \n",
    "    predictions = []\n",
    "    with torch.no_grad():\n",
    "        for x, _ in test_loader:\n",
    "            pred = model(x)\n",
    "            predictions.append(pred.item())\n",
    "            \n",
    "    # Inverse transform\n",
    "    predictions = scaler.inverse_transform(np.array(predictions).reshape(-1, 1)).flatten()\n",
    "    y_true = product_df['quantity'].iloc[-test_days:].values\n",
    "    \n",
    "    # Calculate MAPE\n",
    "    non_zero_idx = y_true != 0\n",
    "    if sum(non_zero_idx) == 0:\n",
    "        mape = 0.0\n",
    "    else:\n",
    "        mape = mean_absolute_percentage_error(y_true[non_zero_idx], predictions[non_zero_idx])\n",
    "        \n",
    "    return model, scaler, mape, y_true, predictions"
